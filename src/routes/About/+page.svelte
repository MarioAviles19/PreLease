<h1>Our Mission</h1>
<section id="main">
	<p>
		&nbsp;&nbsp;&nbsp;&nbsp;According to the census, over 50% of rental households in the Twin
		Cities are "cost-burdened" with racial disparities in both renter distribution and home
		ownership. During a rental emergency, Minnesota tenants can call the tenant hotline, Homeline,
		for legal assistance. Homeline has indicated that tenants are often overwhelmed, uninformed, and
		provide inadequate legal documentation. On top of this, tenants are afraid to speak up, in fear
		of landlord retaliation. In financial emergencies, tenants are often afraid to tell their
		landlord, in fear of an early eviction. When tenants finally access housing services, it's often
		too late as the backrent becomes less manageable for housing agencies and/or there is
		insufficient time to receive assistance. Research (and common sense) indicates people are less
		responsive during stressful situations, making it frustrating for case managers/ social workers,
		often creating a service backlog.
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;Finally, for landlords, evictions are expensive, time-consuming, and lead
		to heated discussions. Even if landlords are paid a large sum through emergency rental assistance,
		it is NOT reliable income and relationship between the tenant and landlord is tarnished, potentially
		leading to non-renewal of lease with the tenant being displaced.
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;The current system/ approach is not meeting the needs of any parties involved.
		The current Minnesota legislature allocating more financial resources to rental assistance does not
		address the underlying structural problems, and tenant protections may unintentially squeeze the
		housing market, where limited affordable units are very competitive for low-income renters.
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;PreLease SBC provides proactive support to renters throughout their entire
		rental journey. We prioritize both housing stability AND housing mobility. We are building a system
		for renter households to meet their immediate needs, and build generational wealth.
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;Housing Search: Assist prospective/ current renters to achieve their housing
		goals (can include home ownership), improve their screening health, self-advocacy, and DISTINGUISH
		themselves from the renter pool
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;Housing Support: Renters have access to the Rental Health Check, a diagnostic
		tool that connects tenants to public resources, improves screening health, and comes with a free
		consultation. For Rental Health Insurance, we assist tenants with early emergency intervention, and
		connect them to resources to reduce stress and rent burden. For landlords and property managers,
		it is mutually beneficial for their tenants to be doing well as it means reliable rent payments and
		positive reputation. This can especially be valuable for affordable housing properties, where we
		can lower the risk of renting to low-income renters.
	</p>
</section>

