<h1>Our Vision</h1>
<section id="main">
	<p>
		&nbsp;&nbsp;&nbsp;&nbsp;We want to uplift our surrounding partners and create an alternative.
		When tenants have a rental emergency, they can reach out to legal assistance such as Homeline,
		and seek out emergency rental assistance. Sometimes there is insufficient time or inadequate
		documentation for legal action. PreLease with our Rental Health Check can proactively assist
		renters before the situation becomes a dire emergency, and refer to our renter support partners
		earlier in the process.
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;We want to demonstrate that proactive support (i.e. Rental Health Insurance)
		for disenfranchised renters leads to measurable benefits in housing stability and Housing mobility.
		Ideally, we want our services to be subsidized for qualified renters by government agencies.
		<br />
		<br />
		&nbsp;&nbsp;&nbsp;&nbsp;As a startup, we will expand our service offering to include management services.
		Our ambition is to Develop an alternative to tenant-screening services and a ranking system that
		rewards housing communities that are providing an enriching environment to their tenants.
	</p>
</section>

